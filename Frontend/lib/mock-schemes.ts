// Helper functions for eligibility and filtering
export function getRecommendedSchemes(
  userProfile: {
    state?: string
    cropType?: string
    landType?: 'owner' | 'tenant'
    season?: 'kharif' | 'rabi' | 'zaid'
  },
  category?: Category
): Scheme[] {
  return mockSchemes.filter((scheme) => {
    if (category && scheme.category !== category) return false
    if (userProfile.state && !scheme.states.includes('all') && !scheme.states.includes(userProfile.state))
      return false
    if (userProfile.cropType && !scheme.cropTypes.includes('all') && !scheme.cropTypes.includes(userProfile.cropType))
      return false
    if (userProfile.landType && !scheme.landTypes.includes(userProfile.landType)) return false
    if (userProfile.season && !scheme.seasons.includes(userProfile.season)) return false
    return true
  })
}

export function getSchemesByCategory(category: Category): Scheme[] {
  return mockSchemes.filter((scheme) => scheme.category === category)
}

export function getSchemeById(id: string): Scheme | undefined {
  return mockSchemes.find((scheme) => scheme.id === id)
}

